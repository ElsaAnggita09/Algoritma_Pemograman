    hasil = None

    if asal == "C":
        if tujuan == "F":
            hasil = (suhu * 9/5) + 32
        elif tujuan == "K":
            hasil = suhu + 273.15
        elif tujuan == "C":
            hasil = suhu
    elif asal == "F":
        if tujuan == "C":
            hasil = (suhu - 32) * 5/9
        elif tujuan == "K":
            hasil = (suhu - 32) * 5/9 + 273.15
        elif tujuan == "F":
            hasil = suhu
    elif asal == "K":
        if tujuan == "C":
            hasil = suhu - 273.15
        elif tujuan == "F":
            hasil = (suhu - 273.15) * 9/5 + 32
        elif tujuan == "K":
            hasil = suhu
    else:
        print("Satuan tidak dikenal!")

    if hasil is not None:
        print(f"Hasil: {hasil}°{tujuan}")

