import math

def operasi_dasar():
    print("\n--- Operasi Dasar ---")
    a = float(input("Masukkan angka pertama: "))
    b = float(input("Masukkan angka kedua: "))
    operator = input("Pilih operator (+, -, *, /): ")

    if operator == "+":
        hasil = a + b
    elif operator == "-":
        hasil = a - b
    elif operator == "*":
        hasil = a * b
    elif operator == "/":
        if b != 0:
            hasil = a / b
        else:
            hasil = "Error: Pembagi tidak boleh nol!"
    else:
        hasil = "Operator tidak valid."

    print("Hasil:", hasil)

def perpangkatan_akar():
    print("\n--- Perpangkatan dan Akar Kuadrat ---")
    pilihan = input("Ketik 'pangkat' untuk perpangkatan atau 'akar' untuk akar kuadrat: ").lower()

    if pilihan == "pangkat":
        angka = float(input("Masukkan angka: "))
