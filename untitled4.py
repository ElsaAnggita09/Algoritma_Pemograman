# 1. Operasi Matrix
class MatrixOperations:
    @staticmethod
    def add(matrix1, matrix2):
        """Penjumlahan dua matriks"""
        if len(matrix1) != len(matrix2) or len(matrix1[0]) != len(matrix2[0]):
            raise ValueError("Ukuran matriks tidak sama")
        return [[matrix1[i][j] + matrix2[i][j] for j in range(len(matrix1[0]))] for i in range(len(matrix1))]

    @staticmethod
    def subtract(matrix1, matrix2):
        """Pengurangan dua matriks"""
        if len(matrix1) != len(matrix2) or len(matrix1[0]) != len(matrix2[0]):
            raise ValueError("Ukuran matriks tidak sama")
        return [[matrix1[i][j] - matrix2[i][j] for j in range(len(matrix1[0]))] for i in range(len(matrix1))]

    @staticmethod
    def multiply(matrix1, matrix2):
        """Perkalian dua matriks"""
        if len(matrix1[0]) != len(matrix2):
            raise ValueError("Jumlah kolom matriks pertama harus sama dengan jumlah baris matriks kedua")
        return [[sum(a*b for a, b in zip(row, col)) for col in zip(*matrix2)] for row in matrix1]

    @staticmethod
    def find_max_min(array):
        """Mencari nilai maksimum dan minimum dalam array"""
        if not array:
            return None, None
        return max(array), min(array)

