def hitung_statistik(teks):
    jumlah_kata = len(teks.split())
    jumlah_karakter = len(teks)
    jumlah_kalimat = teks.count('.') + teks.count('!') + teks.count('?')
    return jumlah_kata, jumlah_karakter, jumlah_kalimat

def cari_kata(teks, kata):
    return kata in teks

def ganti_teks(teks, cari, ganti):
    return teks.replace(cari, ganti)

def format_teks(teks, mode):
    if mode == "uppercase":
        return teks.upper()
    elif mode == "lowercase":
        return teks.lower()
    elif mode == "titlecase":
        return teks.title()
    else:
        return teks

# Program utama
teks_input = input("Masukkan teks: ")

# Statistik
kata, karakter, kalimat = hitung_statistik(teks_input)
print(f"Jumlah kata: {kata}")
print(f"Jumlah karakter: {karakter}")
print(f"Jumlah kalimat: {kalimat}")

# Pencarian kata
