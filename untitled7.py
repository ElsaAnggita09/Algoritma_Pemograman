def faktorial(n):
    if n < 0:
        return "Tidak ada faktorial untuk bilangan negatif"
    if n == 0 or n == 1:
        return 1
    hasil = 1
    for i in range(2, n+1):
        hasil *= i
    return hasil

def rata_rata(data):
    if not data:
        return 0
    return sum(data) / len(data)

def median(data):
    data = sorted(data)
    n = len(data)
    if n == 0:
        return 0
    tengah = n // 2
    if n % 2 == 0:
        return (data[tengah - 1] + data[tengah]) / 2
    else:
        return data[tengah]

